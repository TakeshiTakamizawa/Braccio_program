function [Px, Py] = happy_haruharu(t)

% 0 <= t <= 1
% y方向は全部正
Py = 100;     % 正の位置で固定
scale = 30;

%% ---- ☆（1つ目） ----  (t : 0.00〜0.15)
if t < 0.15
    tau = t / 0.15 * 2*pi;
    Px = 50 + 20 * cos(5*tau);     % ★ 全部正域になるよう +50

%% ---- 「ハ」 ---- (t : 0.15〜0.30)
elseif t < 0.30
    tau = (t-0.15)/0.15;
    Px = 100 + 50 * tau;           % 正の領域のみ

%% ---- 「ッ」 ---- (t : 0.30〜0.40)
elseif t < 0.40
    tau = (t-0.30)/0.10 * 2*pi;
    Px = 160 + 10 * cos(tau);

%% ---- 「ピ」 ---- (t : 0.40〜0.55)
elseif t < 0.55
    tau = (t-0.40)/0.15;
    Px = 190 + 50 * sin(pi * tau);

%% ---- 「ー」 ---- (t : 0.55〜0.65)
elseif t < 0.65
    tau = (t-0.55)/0.10;
    Px = 260 + 60 * tau;

%% ---- ☆（2つ目） ---- (t : 0.65〜0.80)
elseif t < 0.80
    tau = (t-0.65)/0.15 * 2*pi;
    Px = 340 + 20 * cos(5*tau);

%% ---- 「は」 ---- (t : 0.80〜0.90)
elseif t < 0.90
    tau = (t-0.80)/0.10;
    Px = 390 + 40 * sin(pi*tau);

%% ---- 「る」 ---- (t : 0.90〜1.00)
else
    tau = (t-0.90)/0.10 * 2*pi;
    Px = 440 + 20 * cos(tau);
end

end
